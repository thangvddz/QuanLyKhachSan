    private void login() {
        String user = txtMaNV.getText();
        String matKhau = new String(txtMatKhau.getPassword());
        try {
            NhanVien nhanVien = dao.selectByUser(user);
            if (nhanVien != null) {
                String matKhau2 = nhanVien.getPasswd();
                if (matKhau.equals(matKhau2)) {
                    Auth.user = nhanVien;
                    mgsBox.alert(this, "Đăng nhập thành công!");
                    this.dispose();
                } else {                   
                    mgsBox.alert(this, "Sai mật khẩu!");
                }
            } else {
                mgsBox.alert(this, "Sai tên đăng nhập!");
            }
        } catch (Exception e) {
            e.printStackTrace();
            mgsBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }

    private void exit() {
        int hoi = JOptionPane.showConfirmDialog(this, "Bạn muốn thoát thật không?");
        if (hoi != JOptionPane.YES_OPTION) {
            return;
        }
        System.exit(0);
    }
    
