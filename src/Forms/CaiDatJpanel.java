    public void save() {
        if (mgsBox.confirm(this, "Bạn có chắc lưu thay đổi.?")) {
            if (checkText.isHour(txtGioChkIn.getText())) {
                if (checkText.isHour(txtGioChkOut.getText())) {
                    if (checkText.isMinute(txtPhutChkIn.getText())) {
                        if (checkText.isMinute(txtPhutChkOut.getText())) {
                            Config config = new Config(Integer.parseInt(txtGioChkIn.getText()), Integer.parseInt(txtPhutChkIn.getText()), Integer.parseInt(txtGioChkOut.getText()), Integer.parseInt(txtPhutChkOut.getText()));
                            xFile.writeFile(config);
                            mgsBox.alert(this, "Lưu thành công");
                            ManHinhChinhGUI.fillCheckInCheckOut();
                        } else {
                            mgsBox.alert(this, "Phút check-out không đúng định dạng");
                        }
                    } else {
                        mgsBox.alert(this, "Phút check-in không đúng định dạng");
                    }
                } else {
                    mgsBox.alert(this, "Giờ check-out không đúng định dạng");
                }
            } else {
                mgsBox.alert(this, "Giờ check-in không đúng định dạng");
            }
        }
    }
    
    public void fillObjectConfig() {
        Config c = (Config) xFile.readFile();
        if (c == null) {
            clearForm();
        } else {
            fillForm(c);
        }
    }
    
    public void clearForm() {
        txtGioChkIn.setText("");
        txtPhutChkIn.setText("");
        txtGioChkOut.setText("");
        txtPhutChkOut.setText("");
    }
    
    public void fillForm(Config config) {
        txtGioChkIn.setText(String.valueOf(config.getGioChkIn()));
        txtPhutChkIn.setText(String.valueOf(config.getPhutChkIn()));
        txtGioChkOut.setText(String.valueOf(config.getGioChkOut()));
        txtPhutChkOut.setText(String.valueOf(config.getPhutChkOut()));
    }

